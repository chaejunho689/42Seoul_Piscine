int		ft_strcmp(char *s1, char *s2)
{
	int i;
	int j;
	int result;

	i = 0;
	j = 0;
	while (s1[i] != '\0' || s2[j] != '\0')
	{
		if (s1[i] > s2[j])
			result = 1;
		else if (s1[i] < s2[i])
			result = -1;
		else
		{
			result = 0;
		}
		i++;
		j++;
	}
	return (result);
}
