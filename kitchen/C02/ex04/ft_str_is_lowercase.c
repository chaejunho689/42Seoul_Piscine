int		ft_str_is_lowercase(char *str)
{
	int i;
	int j;

	i = 0;
	if (str[0] == '\0')
	{
		j = 1;
	}
	while (str[i] != '\0')
	{
		if (97 <= str[i] && str[i] <= 122)
		{
			i++;
			j = 1;
		}
		else
		{
			j = 0;
			break ;
		}
	}
	return (j);
}
